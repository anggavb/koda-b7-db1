table Transactions;

SELECT t1.customer_id, t1.amount
FROM Transactions t1
WHERE amount > (
    SELECT AVG(amount) FROM transactions t2
    WHERE t1.customer_id = t2.customer_id
);

-- sales
CREATE TABLE Sales (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id INT,
    product VARCHAR(100),
    quantity INT,
    price_per_unit FLOAT(24),
    sales_date TIMESTAMP
);

INSERT INTO Sales (customer_id, product, quantity, price_per_unit, sales_date)
VALUES
    (101, 'Keyboard', 2, 25.00, '2024-04-01'),
    (102, 'Mouse', 1, 15.00, '2024-04-01'),
    (101, 'Monitor', 1, 200.00, '2024-04-02'),
    (103, 'Keyboard', 1, 25.00, '2024-04-02'),
    (101, 'Mouse', 3, 15.00, '2024-04-03');

table sales;

WITH total_sales AS (
    SELECT id, (quantity * price_per_unit) AS total FROM Sales
)
SELECT s.customer_id, ts.total, s.product
FROM Sales s
JOIN total_sales ts ON s.id = ts.id
WHERE s.product = 'Keyboard' AND ts.total > 30;

WITH total_sales AS (
    SELECT product, COUNT(id) as total_sold
    FROM Sales
    GROUP BY product
)
SELECT s.customer_id, s.product, ts.total_sold
FROM Sales s
JOIN total_sales ts ON s.product = ts.product
WHERE ts.total_sold = 1

