table Products;

SELECT MAX(price) FROM Products;

SELECT name, price
FROM Products
WHERE price = (
        SELECT MAX(price)
        FROM Products
    );

SELECT name, price
FROM Products
WHERE price > (
    SELECT AVG(price)
