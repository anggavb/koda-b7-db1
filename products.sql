-- transactions
CREATE TABLE Transactions (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id INT,
    amount INT
);

INSERT INTO Transactions (customer_id, amount)
VALUES
    (101, 500),
    (101, 300),
